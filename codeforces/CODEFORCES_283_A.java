	static long[] bit1, bit2;
	static int total = 1;

	// adds v to element at pos
	public static void update_point (int pos, long v) {
		update_range (pos, pos, v); // probably is a better way to do this
	}

	// adds v to each element in [l, r]
	public static void update_range (int l, int r, long v) {
		update (bit1, l, v); update (bit1, r + 1, -v);
		update (bit2, l, v * (l - 1)); update (bit2, r + 1, -v * r);
	}

	// gets element at pos
	public static long query_point (int pos) {
		return query_range (pos, pos);
		//return query (bit1, pos);
	}

	// gets sum of elements in [l, r]
	public static long query_range (int l, int r) {
		return (query (bit1, r) * r - query (bit2, r)) - (query (bit1, l - 1) * (l - 1) - query (bit2, l - 1));
	}

	private static long query (long[] bit, int pos) {
		long ret = 0;

		for (; pos > 0; pos -= pos & -pos)
			ret += bit[pos];

		return ret;
	}

	private static void update (long[] bit, int pos, long v) {
		for (; pos < bit.length; pos += pos & -pos)
			bit[pos] += v;
	}

